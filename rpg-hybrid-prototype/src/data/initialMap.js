export const findPlayerStart = (map) => {
    for (let y = 0; y < map.length; y++) {
        for (let x = 0; x < map[y].length; x++) {
            if (map[y][x] === 2) {
                return { x, y };
            }
        }
    }
    return { x: 3, y: 3 };
};